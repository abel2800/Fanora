const I18nContext = createContext(null)

export function I18nProvider({ children }) {
  const [language, setLanguageState] = useState(() => localStorage.getItem('fanora_language') || 'en')

  useEffect(() => {
    document.documentElement.lang = language
    localStorage.setItem('fanora_language', language)
  }, [language])

  const value = useMemo(() => ({
    language,
    setLanguage: (next) => setLanguageState(next === 'am' ? 'am' : 'en'),
    toggleLanguage: () => setLanguageState((current) => (current === 'en' ? 'am' : 'en')),
    t: (key) => translations[language]?.[key] || translations.en[key] || key,
  }), [language])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) throw new Error('useI18n must be used inside I18nProvider')
  return context
}
